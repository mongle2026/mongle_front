// 유틸 — patternColorId로 { pattern, color } 객체 반환
export function resolvePatternColor(patternColorId) {
  for (const pattern of PATTERNS) {
    const color = pattern.colors.find(c => c.id === patternColorId);
    if (color) return { pattern, color };
  }
  return null;
}
